def calculate_position_size(
    capital: float,
    risk_per_trade_pct: float,
    entry_price: float,
    stop_loss_price: float,
    max_position_pct: float = 10.0
) -> Union[int, float]:
    """
    Calculate position size based on risk parameters
    
    Args:
        capital: Total trading capital
        risk_per_trade_pct: Risk percentage per trade (e.g., 1.0 for 1%)
        entry_price: Planned entry price
        stop_loss_price: Stop loss price
        max_position_pct: Maximum position size as % of capital
    
    Returns:
        Recommended position quantity
    """
    risk_amount = capital * (risk_per_trade_pct / 100)
    risk_per_unit = abs(entry_price - stop_loss_price)
    
    if risk_per_unit == 0:
        return 0
    
    position_size = risk_amount / risk_per_unit
    
    # Apply max position cap
    max_position_value = capital * (max_position_pct / 100)
    max_qty = max_position_value / entry_price
    
    return min(position_size, max_qty)


def kelly_criterion(
    win_rate: float,
    avg_win: float,
    avg_loss: float
) -> float:
    """
    Calculate Kelly Criterion optimal position size
    
    Args:
        win_rate: Probability of winning (0.0 to 1.0)
        avg_win: Average win amount
        avg_loss: Average loss amount
    
    Returns:
        Kelly fraction (0.0 to 1.0)
    """
    if avg_loss == 0:
        return 0.0
    
    win_loss_ratio = avg_win / avg_loss
    kelly = win_rate - ((1 - win_rate) / win_loss_ratio)
    
    # Clamp to reasonable range
    return max(0.0, min(kelly, 0.5))  # Cap at 50%


