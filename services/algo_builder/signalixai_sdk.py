@dataclass
class MarketData:
    """Market data for a single bar/tick"""
    symbol: str
    timestamp: datetime
    open: float
    high: float
    low: float
    close: float
    volume: int
    vwap: Optional[float] = None
    oi: Optional[int] = None  # Open interest for futures/options


@dataclass
class TickData:
    """Tick-level market data"""
    symbol: str
    timestamp: datetime
    price: float
    quantity: int
    side: str  # 'buy' or 'sell'


@dataclass
class Signal:
    """Trading signal generated by strategy"""
    action: str  # 'buy', 'sell', 'exit_long', 'exit_short'
    symbol: str
    quantity: Union[int, float]
    price: Optional[float] = None
    order_type: str = "market"  # 'market', 'limit', 'stop', 'stop_limit'
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    trailing_stop: Optional[float] = None
    reason: str = ""
    timestamp: Optional[datetime] = None


@dataclass
class Position:
    """Current position information"""
    symbol: str
    side: str  # 'long', 'short', 'flat'
    quantity: Union[int, float]
    avg_entry_price: float
    current_price: float
    unrealized_pnl: float
    realized_pnl: float


@dataclass
class Order:
    """Order information"""
    order_id: str
    symbol: str
    action: str
    quantity: Union[int, float]
    price: Optional[float]
    order_type: str
    status: str  # 'pending', 'open', 'filled', 'cancelled', 'rejected'
    filled_qty: Union[int, float] = 0
    filled_price: Optional[float] = None
    timestamp: datetime = None


