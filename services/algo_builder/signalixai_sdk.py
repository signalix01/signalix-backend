def get_historical_data(
    symbol: str,
    timeframe: str = '1D',
    bars: int = 100,
    data_provider: Optional[Any] = None
) -> List[MarketData]:
    """
    Get historical market data
    
    Args:
        symbol: Trading symbol
        timeframe: Bar timeframe ('1m', '5m', '1h', '1D', etc.)
        bars: Number of bars to retrieve
        data_provider: Data provider instance
    
    Returns:
        List of MarketData objects
    """
    if data_provider:
        return data_provider.get_historical(symbol, timeframe, bars)
    return []


def get_current_price(symbol: str, data_provider: Optional[Any] = None) -> float:
    """
    Get current market price for symbol
    
    Args:
        symbol: Trading symbol
        data_provider: Data provider instance
    
    Returns:
        Current price
    """
    if data_provider:
        return data_provider.get_current_price(symbol)
    return 0.0


