def on_bar(func: Callable) -> Callable:
    """
    Decorator to mark a method as the on_bar event handler.
    Called on every new bar/candle close.
    
    Example:
        @on_bar
        def on_bar(self, data):
            if self.indicators.rsi(14) < 30:
                self.buy()
    """
    func._is_on_bar = True
    return func


def on_tick(func: Callable) -> Callable:
    """
    Decorator to mark a method as the on_tick event handler.
    Called on every tick (optional, for high-frequency strategies).
    
    Example:
        @on_tick
        def on_tick(self, tick):
            if tick.price > self.last_price * 1.01:
                self.buy()
    """
    func._is_on_tick = True
    return func


def on_order_update(func: Callable) -> Callable:
    """
    Decorator to mark a method as the on_order_update event handler.
    Called when order status changes.
    
    Example:
        @on_order_update
        def on_order_update(self, order):
            if order.status == 'filled':
                self.log(f"Order filled: {order.filled_qty} @ {order.filled_price}")
    """
    func._is_on_order_update = True
    return func


def on_position_change(func: Callable) -> Callable:
    """
    Decorator to mark a method as the on_position_change event handler.
    Called when position changes.
    
    Example:
        @on_position_change
        def on_position_change(self, position):
            if position.side == 'long':
                self.stop_loss = position.avg_entry_price * 0.95
    """
    func._is_on_position_change = True
    return func


