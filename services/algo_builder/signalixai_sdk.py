class Strategy(ABC):
    """
    Base class for all trading strategies.
    
    Extend this class and implement on_bar() method to create your strategy.
    
    Example:
        class MyStrategy(Strategy):
            def __init__(self):
                super().__init__()
                self.rsi_period = 14
            
            @on_bar
            def on_bar(self, data):
                rsi = self.indicators.rsi(self.rsi_period)
                if rsi < 30:
                    self.buy(qty=1)
    """
    
    def __init__(self):
        self.name = self.__class__.__name__
        self.parameters: Dict[str, Any] = {}
        self.indicators: Optional[Indicators] = None
        self.data_provider: Optional[Any] = None
        
        # State
        self._position: Optional[Position] = None
        self._orders: List[Order] = []
        self._signals: List[Signal] = []
        self._logs: List[str] = []
        self._equity: float = 0.0
        self._cash: float = 0.0
        
        # Risk management
        self.max_position_size: Union[int, float] = float('inf')
        self.max_daily_loss: float = float('inf')
        self.stop_loss_pct: Optional[float] = None
        self.take_profit_pct: Optional[float] = None
        
        self._discover_handlers()
    
    def _discover_handlers(self):
        """Discover and store event handler methods"""
        self._on_bar_handler = None
        self._on_tick_handler = None
        self._on_order_update_handler = None
        self._on_position_change_handler = None
        
        for attr_name in dir(self):
            attr = getattr(self, attr_name)
            if callable(attr):
                if hasattr(attr, '_is_on_bar'):
                    self._on_bar_handler = attr
                elif hasattr(attr, '_is_on_tick'):
                    self._on_tick_handler = attr
                elif hasattr(attr, '_is_on_order_update'):
                    self._on_order_update_handler = attr
                elif hasattr(attr, '_is_on_position_change'):
                    self._on_position_change_handler = attr
    
    def initialize(self, data_provider: Any, initial_cash: float = 100000.0):
        """Initialize strategy with data provider"""
        self.data_provider = data_provider
        self.indicators = Indicators(data_provider)
        self._cash = initial_cash
        self._equity = initial_cash
    
    # ========================================================================
    # Trading Actions
    # ========================================================================
    
    def buy(self, qty: Union[int, float] = 1, price: Optional[float] = None,
            order_type: str = "market", **kwargs) -> Optional[Signal]:
        """
        Place a buy order
        
        Args:
            qty: Quantity to buy
            price: Limit price (for limit orders)
            order_type: 'market', 'limit', 'stop', 'stop_limit'
            **kwargs: Additional order parameters (stop_loss, take_profit, etc.)
        
        Returns:
            Signal object if order was generated
        """
        # Check position size limit
        if self._position and self._position.side == 'long':
            new_qty = self._position.quantity + qty
            if new_qty > self.max_position_size:
                self.log(f"Buy rejected: would exceed max position size {self.max_position_size}")
                return None
        
        signal = Signal(
            action='buy',
            symbol=self._get_symbol(),
            quantity=qty,
            price=price,
            order_type=order_type,
            stop_loss=kwargs.get('stop_loss'),
            take_profit=kwargs.get('take_profit'),
            trailing_stop=kwargs.get('trailing_stop'),
            reason=kwargs.get('reason', ''),
            timestamp=datetime.now()
        )
        
        self._signals.append(signal)
        self.log(f"BUY signal: {qty} @ {price or 'market'}")
        return signal
    
    def sell(self, qty: Union[int, float] = 1, price: Optional[float] = None,
             order_type: str = "market", **kwargs) -> Optional[Signal]:
        """
        Place a sell order
        
        Args:
            qty: Quantity to sell (negative for short selling)
            price: Limit price
            order_type: 'market', 'limit', 'stop', 'stop_limit'
            **kwargs: Additional order parameters
        
        Returns:
            Signal object if order was generated
        """
        # Check if we have position to sell
        if not self._position or self._position.side != 'long':
            self.log("Sell rejected: no long position to exit")
            return None
        
        if qty > self._position.quantity:
            qty = self._position.quantity
        
        signal = Signal(
            action='sell',
            symbol=self._get_symbol(),
            quantity=qty,
            price=price,
            order_type=order_type,
            reason=kwargs.get('reason', ''),
            timestamp=datetime.now()
        )
        
        self._signals.append(signal)
        self.log(f"SELL signal: {qty} @ {price or 'market'}")
        return signal
    
    def exit_position(self, **kwargs) -> Optional[Signal]:
        """
        Exit current position (either long or short)
        
        Returns:
            Signal object if position was exited
        """
        if not self._position or self._position.side == 'flat':
            self.log("Exit rejected: no open position")
            return None
        
        if self._position.side == 'long':
            return self.sell(qty=self._position.quantity, reason="Exit position", **kwargs)
        else:
            # Short position - would need buy_to_cover
            self.log("Exit short position - not yet implemented")
            return None
    
    def cancel_all_orders(self) -> List[str]:
        """Cancel all pending orders"""
        cancelled = []
        for order in self._orders:
            if order.status == 'pending':
                order.status = 'cancelled'
                cancelled.append(order.order_id)
        self.log(f"Cancelled {len(cancelled)} orders")
        return cancelled
    
    # ========================================================================
    # Position Information
    # ========================================================================
    
    def get_position(self) -> Optional[Position]:
        """Get current position information"""
        return self._position
    
    def is_in_position(self) -> bool:
        """Check if strategy has an open position"""
        return self._position is not None and self._position.side != 'flat'
    
    def is_long(self) -> bool:
        """Check if currently in long position"""
        return self._position is not None and self._position.side == 'long'
    
    def is_short(self) -> bool:
        """Check if currently in short position"""
        return self._position is not None and self._position.side == 'short'
    
    def get_quantity(self) -> Union[int, float]:
        """Get current position quantity"""
        if self._position:
            return self._position.quantity
        return 0
    
    def get_pnl(self) -> Dict[str, float]:
        """Get current P&L information"""
        if not self._position:
            return {'unrealized': 0.0, 'realized': 0.0, 'total': 0.0}
        
        return {
            'unrealized': self._position.unrealized_pnl,
            'realized': self._position.realized_pnl,
            'total': self._position.unrealized_pnl + self._position.realized_pnl
        }
    
    # ========================================================================
    # Utility Methods
    # ========================================================================
    
    def log(self, message: str) -> None:
        """Log a message (appears in strategy logs)"""
        timestamp = datetime.now().strftime("%H:%M:%S")
        log_entry = f"[{timestamp}] {message}"
        self._logs.append(log_entry)
        logger.info(f"{self.name}: {message}")
    
    def get_logs(self) -> List[str]:
        """Get all strategy logs"""
        return self._logs.copy()
    
    def clear_logs(self) -> None:
        """Clear strategy logs"""
        self._logs.clear()
    
    def get_equity(self) -> float:
        """Get current account equity"""
        return self._equity
    
    def get_cash(self) -> float:
        """Get available cash"""
        return self._cash
    
    def get_signals(self) -> List[Signal]:
        """Get all generated signals (and clear them)"""
        signals = self._signals.copy()
        self._signals.clear()
        return signals
    
    def _get_symbol(self) -> str:
        """Get current trading symbol"""
        if self.data_provider:
            return getattr(self.data_provider, 'symbol', 'UNKNOWN')
        return 'UNKNOWN'
    
    # ========================================================================
    # Event Handlers (to be implemented by subclasses)
    # ========================================================================
    
    def on_bar(self, data: MarketData) -> None:
        """
        Called on every new bar/candle close.
        Override this method or use @on_bar decorator.
        
        Args:
            data: MarketData for the completed bar
        """
        if self._on_bar_handler:
            self._on_bar_handler(self, data)
    
    def on_tick(self, tick: TickData) -> None:
        """
        Called on every tick.
        Override this method or use @on_tick decorator.
        
        Args:
            tick: TickData for the tick
        """
        if self._on_tick_handler:
            self._on_tick_handler(self, tick)
    
    def on_order_update(self, order: Order) -> None:
        """
        Called when order status changes.
        Override this method or use @on_order_update decorator.
        
        Args:
            order: Order with updated status
        """
        if self._on_order_update_handler:
            self._on_order_update_handler(self, order)
    
    def on_position_change(self, position: Position) -> None:
        """
        Called when position changes.
        Override this method or use @on_position_change decorator.
        
        Args:
            position: Updated position information
        """
        self._position = position
        if self._on_position_change_handler:
            self._on_position_change_handler(self, position)


