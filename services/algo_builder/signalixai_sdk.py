class Indicators:
    """
    Technical indicators helper class.
    Provides access to TA-Lib functions and custom indicators.
    """
    
    def __init__(self, data_provider: Any):
        self._data = data_provider
        self._cache: Dict[str, Any] = {}
    
    def _get_series(self, field: str = 'close', period: int = 100) -> List[float]:
        """Get historical price series"""
        return self._data.get_series(field, period)
    
    def sma(self, period: int = 20, field: str = 'close') -> float:
        """Simple Moving Average"""
        series = self._get_series(field, period)
        if len(series) < period:
            return series[-1] if series else 0.0
        return sum(series[-period:]) / period
    
    def ema(self, period: int = 20, field: str = 'close') -> float:
        """Exponential Moving Average"""
        series = self._get_series(field, period * 2)
        if len(series) < period:
            return series[-1] if series else 0.0
        
        multiplier = 2 / (period + 1)
        ema = series[0]
        for price in series[1:]:
            ema = (price - ema) * multiplier + ema
        return ema
    
    def rsi(self, period: int = 14) -> float:
        """
        Relative Strength Index
        Returns RSI value between 0-100
        """
        closes = self._get_series('close', period * 2)
        if len(closes) < period + 1:
            return 50.0  # Neutral
        
        gains = []
        losses = []
        
        for i in range(1, len(closes)):
            change = closes[i] - closes[i-1]
            if change > 0:
                gains.append(change)
                losses.append(0)
            else:
                gains.append(0)
                losses.append(abs(change))
        
        if len(gains) < period:
            return 50.0
        
        avg_gain = sum(gains[-period:]) / period
        avg_loss = sum(losses[-period:]) / period
        
        if avg_loss == 0:
            return 100.0
        
        rs = avg_gain / avg_loss
        rsi = 100 - (100 / (1 + rs))
        return rsi
    
    def macd(self, fast: int = 12, slow: int = 26, signal: int = 9) -> Dict[str, float]:
        """
        MACD (Moving Average Convergence Divergence)
        Returns dict with 'macd', 'signal', 'histogram'
        """
        closes = self._get_series('close', slow + signal)
        
        ema_fast = self._calculate_ema(closes, fast)
        ema_slow = self._calculate_ema(closes, slow)
        
        macd_line = ema_fast - ema_slow
        signal_line = self._calculate_ema([macd_line] * (signal + 1), signal)
        histogram = macd_line - signal_line
        
        return {
            'macd': macd_line,
            'signal': signal_line,
            'histogram': histogram
        }
    
    def _calculate_ema(self, data: List[float], period: int) -> float:
        """Helper to calculate EMA from a list"""
        if len(data) < period:
            return data[-1] if data else 0.0
        
        multiplier = 2 / (period + 1)
        ema = sum(data[:period]) / period
        
        for price in data[period:]:
            ema = (price - ema) * multiplier + ema
        
        return ema
    
    def bollinger_bands(self, period: int = 20, std_dev: float = 2.0) -> Dict[str, float]:
        """
        Bollinger Bands
        Returns dict with 'upper', 'middle', 'lower'
        """
        closes = self._get_series('close', period)
        if len(closes) < period:
            current = closes[-1] if closes else 0.0
            return {'upper': current, 'middle': current, 'lower': current}
        
        middle = sum(closes[-period:]) / period
        variance = sum((x - middle) ** 2 for x in closes[-period:]) / period
        std = variance ** 0.5
        
        return {
            'upper': middle + (std_dev * std),
            'middle': middle,
            'lower': middle - (std_dev * std)
        }
    
    def atr(self, period: int = 14) -> float:
        """
        Average True Range
        Volatility indicator
        """
        highs = self._get_series('high', period + 1)
        lows = self._get_series('low', period + 1)
        closes = self._get_series('close', period + 1)
        
        if len(highs) < 2:
            return 0.0
        
        true_ranges = []
        for i in range(1, len(highs)):
            tr1 = highs[i] - lows[i]
            tr2 = abs(highs[i] - closes[i-1])
            tr3 = abs(lows[i] - closes[i-1])
            true_ranges.append(max(tr1, tr2, tr3))
        
        if len(true_ranges) < period:
            return sum(true_ranges) / len(true_ranges) if true_ranges else 0.0
        
        return sum(true_ranges[-period:]) / period
    
    def adx(self, period: int = 14) -> float:
        """
        Average Directional Index
        Trend strength indicator (0-100)
        """
        # Simplified ADX calculation
        highs = self._get_series('high', period * 2)
        lows = self._get_series('low', period * 2)
        
        if len(highs) < period * 2:
            return 25.0  # Neutral
        
        # Calculate +DM and -DM
        plus_dm = []
        minus_dm = []
        
        for i in range(1, len(highs)):
            up_move = highs[i] - highs[i-1]
            down_move = lows[i-1] - lows[i]
            
            if up_move > down_move and up_move > 0:
                plus_dm.append(up_move)
                minus_dm.append(0)
            elif down_move > up_move and down_move > 0:
                plus_dm.append(0)
                minus_dm.append(down_move)
            else:
                plus_dm.append(0)
                minus_dm.append(0)
        
        if len(plus_dm) < period:
            return 25.0
        
        atr_val = self.atr(period)
        if atr_val == 0:
            return 25.0
        
        plus_di = 100 * sum(plus_dm[-period:]) / (period * atr_val)
        minus_di = 100 * sum(minus_dm[-period:]) / (period * atr_val)
        
        dx = 100 * abs(plus_di - minus_di) / (plus_di + minus_di) if (plus_di + minus_di) > 0 else 0
        return dx
    
    def stochastic(self, k_period: int = 14, d_period: int = 3) -> Dict[str, float]:
        """
        Stochastic Oscillator
        Returns dict with '%K' and '%D'
        """
        highs = self._get_series('high', k_period)
        lows = self._get_series('low', k_period)
        closes = self._get_series('close', k_period)
        
        if len(highs) < k_period:
            return {'%K': 50.0, '%D': 50.0}
        
        highest_high = max(highs[-k_period:])
        lowest_low = min(lows[-k_period:])
        current_close = closes[-1]
        
        if highest_high == lowest_low:
            k = 50.0
        else:
            k = 100 * (current_close - lowest_low) / (highest_high - lowest_low)
        
        # Simplified %D (using last k values)
        d = k  # In full implementation, this would be SMA of %K
        
        return {'%K': k, '%D': d}
    
    def vwap(self) -> float:
        """
        Volume Weighted Average Price
        """
        highs = self._get_series('high', 100)
        lows = self._get_series('low', 100)
        closes = self._get_series('close', 100)
        volumes = self._get_series('volume', 100)
        
        if not volumes or sum(volumes) == 0:
            return closes[-1] if closes else 0.0
        
        typical_prices = [(h + l + c) / 3 for h, l, c in zip(highs, lows, closes)]
        vwap = sum(tp * vol for tp, vol in zip(typical_prices, volumes)) / sum(volumes)
        
        return vwap


